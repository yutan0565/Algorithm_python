A,B = map(int, input().split())

def add(a,b):
    return a+b

def subtract(a,b):
    return a-b

def multiply(a,b):
    return a*b

def divide(a,b):
    return a//b

def divide_re(a,b):
    return a%b

print(add(A,B))
print(subtract(A,B))
print(multiply(A,B))
print(divide(A,B))
print(divide_re(A,B))
